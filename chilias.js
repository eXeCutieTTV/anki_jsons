    const greekDict = {};
    let currentLetter = null;
    let j = 0;
    greekArray.forEach(entry => {
        // If the entry is a single Greek letter, set it as the current header
        if (entry.length === 1 && /[Α-Ω]/.test(entry)) {
            currentLetter = entry;
            greekDict[currentLetter] = [];
        } else if (currentLetter) {
            // Otherwise, add the entry under the current letter
            const split = entry.split(/,(.+)/);
            greekDict[currentLetter].push({ "Front": split[0], "Back": split[1] });
        }
        j++
    });

